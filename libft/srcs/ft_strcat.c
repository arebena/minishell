#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int e;

	e = 0;
	i = ft_strlen(s1);
	while (s2[e] != 0)
		s1[i++] = s2[e++];
	s1[i] = 0;
	return (s1);
}
