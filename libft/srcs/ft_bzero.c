#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*e;

	i = 0;
	if ((char)n <= 0)
		return ;
	e = s;
	while (n--)
		e[i++] = 0;
	s = e;
	return ;
}
