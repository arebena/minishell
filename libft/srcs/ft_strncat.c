#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int e;

	i = 0;
	e = 0;
	while (s1[i] != 0)
		i++;
	while (e < (int)n)
		s1[i++] = s2[e++];
	s1[i] = 0;
	return (s1);
}
