#include "libft.h"

void	ft_btabdl(char ***tab)
{
	int i;

	i = 0;
	if (tab)
		if (*tab)
			while ((*tab)[i++])
				ft_strdel(&((*tab)[i - 1]));
	*tab = NULL;
}
