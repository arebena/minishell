#include "libft.h"

int		ft_nbrlen(char *nbr)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	if (nbr[i] == '-' || nbr[i] == '+')
		i = ++len;
	while (nbr[i] && nbr[i] >= '0' && nbr[i] <= '9')
	{
		len++;
		i++;
	}
	return (len);
}
